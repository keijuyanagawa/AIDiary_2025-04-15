# --- Instructions for Supabase Setup ---

# 1. Create a Supabase Project:
#    - Go to https://supabase.com/ and create a new project.
#    - Note your Project URL and anon key (public key).

# 2. Create Tables in Supabase:
#    - Use the Supabase Table Editor UI or SQL Editor.
#    - Create the following tables:

#    a) `entries` table:
#       Columns:
#       - id:           INT8 (Primary Key, auto-incrementing) - or UUID
#       - created_at:   TIMESTAMPZ (Default: now())
#       - body:         TEXT
#       - summary:      TEXT (nullable)
#       (Note: `user_id` column is removed as login is disabled)
#       Enable RLS (Row Level Security) if needed, but for a no-login app,
#       you might leave it disabled or set a public read/write policy initially.
#       Example SQL (using SQL Editor):
#       ```sql
#       CREATE TABLE public.entries (
#         id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
#         created_at timestamp with time zone DEFAULT now() NOT NULL,
#         body text,
#         summary text
#       );
#       -- Optional: Indexes for performance
#       CREATE INDEX idx_entries_created_at ON public.entries USING btree (created_at);
#       ```

#    b) `emotions` table:
#       Columns:
#       - id:            INT8 (Primary Key, auto-incrementing) - or UUID
#       - entry_id:      INT8 (Foreign Key referencing entries.id)
#       - recorded_at:   TIMESTAMPZ (Default: now())
#       - emotion_label: TEXT NOT NULL
#       - emotion_score: REAL (nullable)
#       Enable RLS as needed. Consider CASCADE DELETE on the foreign key.
#       Example SQL (using SQL Editor):
#       ```sql
#       CREATE TABLE public.emotions (
#         id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
#         entry_id bigint REFERENCES public.entries(id) ON DELETE CASCADE NOT NULL,
#         recorded_at timestamp with time zone DEFAULT now() NOT NULL,
#         emotion_label text NOT NULL,
#         emotion_score real
#       );
#       -- Optional: Indexes for performance
#       CREATE INDEX idx_emotions_entry_id ON public.emotions USING btree (entry_id);
#       CREATE INDEX idx_emotions_recorded_at ON public.emotions USING btree (recorded_at);
#       CREATE INDEX idx_emotions_label ON public.emotions USING btree (emotion_label);
#       ```

# 3. Set Environment Variables:
#    - Create a `.env` file in your project root.
#    - Add your Supabase URL and anon key:
#      ```
#      SUPABASE_URL="YOUR_PROJECT_URL"
#      SUPABASE_KEY="YOUR_ANON_KEY"
#      GEMINI_API_KEY="YOUR_GEMINI_KEY" # Keep if using AI features
#      ```
#    - Make sure `.env` is listed in your `.gitignore` file.

# 4. Install Python Libraries:
#    - Update your `requirements.txt`:
#      ```
#      streamlit
#      python-dotenv
#      google-generativeai
#      pandas
#      numpy
#      matplotlib
#      streamlit-calendar
#      supabase
#      # bcrypt is no longer needed
#      ```
#    - Run: pip install -r requirements.txt

